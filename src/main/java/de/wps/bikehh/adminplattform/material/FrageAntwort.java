package de.wps.bikehh.adminplattform.material;

public class FrageAntwort {

	private int meldungId;
	private int frageId;
	private String text;

	public int getMeldungId() {
		return meldungId;
	}

	public void setMeldungId(int meldungId) {
		this.meldungId = meldungId;
	}

	public int getFrageId() {
		return frageId;
	}

	public void setFrageId(int frageId) {
		this.frageId = frageId;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

}
